package hoang.com.baith9_recyclerview;

public class Landscape {
    private String Name;
    private String Img;

    public Landscape() {
    }

    public Landscape(String name, String img) {
        Name = name;
        Img = img;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getImg() {
        return Img;
    }

    public void setImg(String img) {
        Img = img;
    }
}
